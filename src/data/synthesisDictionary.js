/**
 * 通変星ペアフレーズを取得する(A_B なければ B_A を検索、それもなければ default)
 * @param {string} selfTopGod - 自分の最強通変星
 * @param {string} partnerTopGod - 相手の最強通変星
 * @returns {Object} { core, detail, warning }
 */
export function getTopGodPair(selfTopGod, partnerTopGod) {
  const dict = SYNTHESIS_DICT_JA.topGodPair;
  return dict[`${selfTopGod}_${partnerTopGod}`]
      || dict[`${partnerTopGod}_${selfTopGod}`]
      || dict.default;
}

