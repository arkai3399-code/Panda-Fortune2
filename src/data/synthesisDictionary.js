/**
 * フレーズ内の {var} 形式のプレースホルダを置換する
 * @param {string} template - '{year}年は...' 形式のテンプレート文字列
 * @param {Object} vars - 置換する変数のマップ 例: { year: 2026, target: 'あなた' }
 * @returns {string} 置換後の文字列
 *
 * @example
 *   fillTemplate('{year}年は{element}の年ンダ', { year: 2026, element: '火' })
 *   // => '2026年は火の年ンダ'
 */
export function fillTemplate(template, vars) {
  return template.replace(/\{(\w+)\}/g,
    (_, key) => String(vars[key] ?? `{${key}}`));
}

