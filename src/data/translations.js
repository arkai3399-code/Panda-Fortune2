  function translateNode(root, lang){
    if(!root)return;
    var w=document.createTreeWalker(root,NodeFilter.SHOW_TEXT,null,false);
    var nodes=[],n;
    while((n=w.nextNode()))nodes.push(n);
    nodes.forEach(function(node){
      var txt=(node.textContent||'').trim();
      if(!txt)return;
      var p=node.parentElement;
      if(!p||p.tagName==='SCRIPT'||p.tagName==='STYLE'||p.tagName==='NOSCRIPT')return;
      var tr;
      if(lang==='jp'){if(REV[txt])tr=REV[txt];}
      else{
        if(T[txt]&&T[txt][lang])tr=T[txt][lang];
        else if(REV[txt]){var j=REV[txt];if(T[j]&&T[j][lang])tr=T[j][lang];}
      }
      if(tr&&tr!==node.textContent)node.textContent=tr;
    });
  }
