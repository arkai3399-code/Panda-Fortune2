export function getGogyoTraitsByLang(lang) {
  if (lang === 'kr' && Object.keys(GOGYO_TRAITS_KR).length > 0) {
    // KR と JA をマージ（欠損時は JA 値）
    const merged = {};
    for (const k of Object.keys(GOGYO_TRAITS_JA)) {
      merged[k] = { ...GOGYO_TRAITS_JA[k], ...(GOGYO_TRAITS_KR[k] || {}) };
    }
    return merged;
  }
  return GOGYO_TRAITS_JA;
}

